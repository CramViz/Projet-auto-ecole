    private void btnValiderMoniteurMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnValiderMoniteurMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String dateNaiss = sdf.format(jcalDateNaiss.getDate());
        String prenom = txtPrenomMoniteur.getText();
        String nom = txtNomMoniteur.getText();
        String adresse = txtAdresse2.getText();
        String ville = txtVille.getText();
        String codePostal = txtPostalMoniteur.getText();
        String telephone = txtTelephoneMoniteur.getText();
        String libSexe = cboSexe.getSelectedItem().toString();
        int sexe = 0;
        if(libSexe == "Homme"){
            sexe = 1;
        }
        if(libSexe == "Femme"){
            sexe = 2;
        }
        if(libSexe == "Non binaire"){
            sexe = 3;
        }

        if(txtIDMono.getText() == "")
        {
            ctrlMoniteur.addMoniteur(ctrlMoniteur.getLastIDMoniteur()+1, nom, prenom, sexe, adresse, telephone, codePostal, ville, dateNaiss);
        }
        else
        {
            int id = Integer.parseInt(txtIDMono.getText());
            System.out.println(id);
            ctrlMoniteur.modifierUnMoniteur(id, nom, prenom, adresse, telephone, sexe, codePostal, ville, dateNaiss);
        }

        mdlMoniteur = new ModelJTable();
        mdlMoniteur.loadDatasMoniteur(ctrlMoniteur.getAllMoniteur());
        tblMoniteur.setModel(mdlMoniteur);
        
        txtIDMono.setText("");
        txtAdresse2.setText("");
        txtNomMoniteur.setText("");
        txtPostalMoniteur.setText("");
        txtPrenomMoniteur.setText("");
        txtTelephoneMoniteur.setText("");
        txtVille.setText("");
        
        

    }//GEN-LAST:event_btnValiderMoniteurMouseClicked

