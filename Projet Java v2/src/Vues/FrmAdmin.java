    private void btnValiderVehiculeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnValiderVehiculeMouseClicked
        // TODO add your handling code here:
        if (jcalAnnee.getYear() == 0 || txtMarque.getText().length() == 0 || txtImmatriculation.getText().length() == 0 || txtModele.getText().length() == 0)
        {
            lblError.setText("Veuillez saisir toutes les informations");
        }
        else
        {
            int annee = jcalAnnee.getYear();
            String imma = txtImmatriculation.getText();
            String marque = txtMarque.getText();
            String modele = txtModele.getText();
            int codeCat = ctrlCategorie.getCodeCatByLib(cboCategorie.getSelectedItem().toString());
            if(tblVehicule.getSelectedRow() == -1)
            {
                ctrlVehicule.addVehicule(imma, annee, modele, marque, codeCat);
            }
            else
            {
                String matricule =  (String)tblVehicule.getValueAt(tblVehicule.getSelectedRow(), 0);
                ctrlVehicule.modifierVehicule(matricule, annee, modele, marque, codeCat);
            }

            mdlVehicule = new ModelJTable();
            mdlVehicule.loadDatasVehicule(ctrlVehicule.getAllVehicule());
            tblVehicule.setModel(mdlVehicule);
        }
        
        
    }//GEN-LAST:event_btnValiderVehiculeMouseClicked

